            FUNCTION X0(AK)
      IMPLICIT NONE
      REAL X0,XI,XM,XS,F
      REAL AK
      REAL T,APS,PAS
      INTEGER IITER,ITOUR
      F(T)=AK-T*TANH(T)
      APS=5.E-6
      ITOUR=0
      XI=0.
      XS=XI
      PAS=AMAX1(AK,SQRT(AK))
   30 XS=XS+PAS
      ITOUR=ITOUR+1
      IF(ITOUR.LE.1000)THEN
      IF(F(XS)*F(XI).GT.0)GOTO 30
      ENDIF
      IITER=0
   10 CONTINUE
      XM=(XI+XS)*0.5
      IITER=IITER+1
      IF(IITER.GT.1000.OR.ITOUR.GT.1000)THEN
      WRITE(*,110)ITOUR,IITER
  110 FORMAT(2X,'ERREUR DANS LA RECHERCHE DE LA RACINE',&
     &/2X,'APPROXIMATION =',I5,'   DICHOTOMIE = ',I5)
      STOP
      ELSE
      IF(ABS((XS-XI)/XM).GT.APS)THEN
      IF(F(XM)*F(XI).LT.0)THEN
      XS=XM
      ELSE
      XI=XM
      ENDIF
      GOTO 10
      ELSE
      X0=XM
      ENDIF
      ENDIF
      RETURN
      END
